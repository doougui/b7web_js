// Includes

// Checa se contém o item no array
let lista = ['Ovo', 'Café', 'Arroz', 'Feijão', 'Macarrão'];

console.log( lista.includes('Ovo') ); // True
console.log( lista.includes('Carne') ); // False

// Checa se uma string contém uma letra ou parte especificada
let nome = 'Douglas';

console.log( nome.includes('s') ); // True
console.log( nome.includes('z') ); // False


// Repeat

// Printa um 'x' 20 vezes
console.log('x'.repeat(20)); // xxxxxxxxxxxxxxxxxxxx