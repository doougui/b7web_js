console.log(`PI: ${Math.PI}`); // Valor de PI
console.log(`MATH ROUND: ${Math.round(3.5)}`); // Arredondamento dependente do número (3.4 => 3, 3.6 => 4, 3.5 => 4)
console.log(`MATH FLOOR: ${Math.floor(3.9)}`); // Arredondamento para baixo
console.log(`MATH CEIL: ${Math.ceil(3.1)}`); // Arredondamento para cima
console.log(`MATH ABS: ${Math.abs(-9.2432341)}`); // Número absoluto (positivo)
console.log(`MATH MIN: ${Math.min(7, 100, 600, 20, 3)}`); // Retorna o menor número dos informados
console.log(`MATH MAX: ${Math.max(7, 100, 600, 20, 3)}`); // Retorna o maior número dos informados
console.log(`MATH RANDOM: ${Math.random()}`); // Retorna um número aleatório entre 0 e 1
console.log(`NÚMERO ALEATÓRIO ENTRE 0 E X: ${Math.floor(Math.random() * 100)}`); // Retorna um número aleatório entre 0 e X